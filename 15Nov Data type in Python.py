#Tuple:- A sequence of characters in between a parathesis (1,2,3,4,5), immutable data type. 
#Note: we con not update the data. 
a = (20,-30.05,"""s""") 
print(a) 
print("The type of data",type(a)) 

a = (10.105+10,0.2-0.4,2*2,10/2) 
print(a) 
print("The type of data",type(a))

a = (20,-30.05,"shiv","2345",5+5) 
print(a) 
print("The type of data",type(a)) 

#Set:- A sequence of characters in between a curly braces {1,2,3,4,5}, does not allow duplicate values. 
a = {1,2,3,4,5,6} 
print(a) 
print("The type of data",type(a)) 

a = {1+1,2-1,3*3} 
print(a) 
print("The type of data",type(a)) 

a = {1+1,2-1,3*3,"shiv"} 
print(a) 
print("The type of data",type(a)) 

